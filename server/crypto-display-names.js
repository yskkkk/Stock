/**
 * @param {string} base Binance 베이스 (예: BTC, 1000PEPE)
 */
export function cryptoSpotDisplayName(base) {
  const b = String(base ?? "").toUpperCase();
  let row = BY_BASE[b];
  if (!row && /^[0-9]+[A-Z]/.test(b)) {
    const stripped = b.replace(/^[0-9]+/, "");
    row = BY_BASE[stripped];
  }
  if (!row) return b;
  if (row.en && row.ko && row.en !== row.ko) return `${row.en} / ${row.ko}`;
  return row.ko;
}

/**
 * @param {string} yahooSymbol 예: BTC-USDT
 */
export function cryptoYahooUsdtDisplayName(yahooSymbol) {
  const sym = String(yahooSymbol ?? "").trim().toUpperCase();
  const m = /^([A-Z0-9]+)-USDT$/.exec(sym);
  if (!m) return sym;
  return cryptoSpotDisplayName(m[1]);
}

